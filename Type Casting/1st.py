a = 56
t = type(a) # This will return the data type of variable a, which is int (integer).
print(t) # This will print the data type of variable a, which is <class 'int'>.

b = 4.7
t = type(b) # This will return the data type of variable b, which is float.
print(t) # This will print the data type of variable b, which is <class 'float'>.

c = "Pankaj"
t = type(c) # This will return the data type of variable c, which is str (string).
print(t) # This will print the data type of variable c, which is <class 'str'>.