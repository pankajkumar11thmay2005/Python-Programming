# Input() Function
# The input() function is used to take input from the user. It takes a string as an argument, which is displayed as a prompt to the user. The function returns the input as a string.

a = input("Enter Number 1: ")  
b = input("Enter Number 2: ")

print("Number 1 is: ", a)
print("Number 2 is: ", b)

print("Sum is : ", a + b)

a = 7
b = 3
