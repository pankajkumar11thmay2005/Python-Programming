s = set()
print(s)

s = set("pankajkumar")
print(s)

# Creating a Set with the use of a List
s = set(["pnkj", "rhul", "vanshm"])
print(s)

# Creating a Set with the use of a tuple
t = ("pnkj", "rhul", "vanshm")
print(set(t))

# Creating a Set with the use of a dictionary
d = {"pnkj": 1, "rhul": 2, "vanshm": 3}
print(set(d))