# Assignment Operators in Python
a = 8
b = 9
print(a)  # Output: 8
print(b)  # Output: 9
a += 2  # Equivalent to a = a + 2
b -= 3  # Equivalent to b = b - 3
print(a)  # Output: 10
print(b)  # Output: 6
a *= 2  # Equivalent to a = a * 2
b /= 2  # Equivalent to b = b / 2
print(a)  # Output: 20
print(b)  # Output: 3.0
a %= 3  # Equivalent to a = a % 3
b **= 2  # Equivalent to b = b ** 2
print(a)  # Output: 2
print(b)  # Output: 9.0
a //= 2  # Equivalent to a = a // 2
b //= 2  # Equivalent to b = b // 2
print(a)  # Output: 1
print(b)  # Output: 4.0
