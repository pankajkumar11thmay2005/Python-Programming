# Comparison Operators in Python
a = 8
b = 9
print(a == b)  # Equal to
print(a != b)  # Not equal to
print(a > b)   # Greater than
print(a < b)   # Less than
print(a >= b)  # Greater than or equal to
print(a <= b)  # Less than or equal to

d = a < b
print(d)  # Output: True

d = 8 > 9
print(d)  # Output: False

d = 8 == 9
print(d)  # Output: False

d = 8 != 9
print(d)  # Output: True

d = 8 >= 9
print(d)  # Output: False

d = 8 <= 9
print(d)  # Output: True
